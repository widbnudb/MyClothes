import mysql.connector
from mysql.connector import Error


class DataBase:
    def __init__(self):
        self.conn = mysql.connector.connect(host='localhost', database='myclothes', user='Elizabeth', password='12345678')
        self.cursor = self.conn.cursor()

    def add_to_wardrobe(self, result, file_name):
        query = "INSERT INTO wardrobe VALUES(%s,%s)"
        args = (result, file_name)
        self.cursor.execute(query, args)
        self.conn.commit()
        self.cursor.close()
        self.conn.close()

    def get_from_db(self, wardrobe_part):
        file_name_list = []
        query = "SELECT file_name FROM wardrobe WHERE class_name = '%s'" % (wardrobe_part)
        self.cursor.execute(query)
        results = self.cursor.fetchall()
        for elem in results:
            file_name_list.append(elem)
        self.cursor.close()
        self.conn.close()
        return file_name_list


